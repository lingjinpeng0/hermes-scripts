bd('1. 根据你所学，陈述方差分析的基本解题思路和步骤')
bd('答：方差分析根据平方和的加和性原理，将总平方和分解为不同影响因素产生的平方和以及代表随机变化的误差平方和，用F检验分析各因素对总平方和贡献的显著程度。')
bd('步骤：a.将总平方和和总自由度分解为各变异因素的平方和和自由度；b.建立方差分析表，计算各项均方并进行F检验；c.若F检验显著，对各处理平均数进行多重比较。')

bd('2. 什么叫区组？什么叫随机化完全区组设计？这种设计的主要目的是什么？')
bd('答：①区组指性质和条件相同的试验单元构成的组群。②随机化完全区组设计：在一个区组内，每个处理完全随机配置给各试验单元，所有处理重复数相同，各区组含量相同。③主要目的：使区组内部条件尽可能一致，达到局部控制的目的。')

bd('3. 什么是试验单元，什么是抽样单元？两者的含义相同吗？举例说明')
bd('答：①试验单元：试验人员施加处理的对象单位。②抽样单元：试验单元的某个分段。③两者不完全相同。例如网箱养鱼试验中，一个网箱内所有鱼是一个试验单元，而每一条鱼为抽样单元。')

bd('4. 研究两个变量之间的关系时，何时采用回归分析？何时采用相关分析？')
bd('答：（1）区别：回归分析研究一个随机变量y与另一些变量的函数关系，用于预测；相关分析研究随机变量之间的共变关系，通过相关系数判断相关强弱。')
bd('（2）联系：b与r的显著性检验性质相同，b显著则r必显著，反之亦然。')
bd('（3）应用：若自变量为固定变量，只能做回归分析；若两个变量均为随机变量，两者均可做，且显著性一致。')

bd('5. 进行平均数比较时，何时采用假设检验，何时采用方差分析？举例说明')
bd('答：两个样本平均数比较（或一个样本与已知值比较）用假设检验，如比较两个湖泊2龄黄颡鱼平均体重的差异。三个及以上样本平均数比较用方差分析，如比较5种蛋白水平饲料对黄颡鱼生长的影响。')

bd('6. 什么是因子水平？什么是试验处理？两者之间有何区别与联系？')
bd('答：①因子水平：一个试验因子的不同数量等级或不同状态。②试验处理：施加给试验单元的条件。③联系：单因子试验中，因子的一水平就是一种处理；多因子试验中，不同因子不同水平的组合叫处理。')

bd('7. 什么是质量性状资料？其获得方法有哪些？请举例说明')
bd('答：①质量性状资料指能观测但不能直接测量的性状资料，如颜色、性别等。②获得方法：统计次数法（按类别分组统计次数）；评分或评级法（如-、+、++、+++分级后统计次数）。')

bd('8. 如何选择适合的曲线方程对变量间的函数关系进行拟合？判定直线回归方程和曲线回归方程拟合度的指标和方法有何异同？')
bd('答：（1）选择方法：①理论法（经验法）根据专业知识选择；②图示法根据散点图趋势对照曲线图谱；③拟合比较法同时拟合多个曲线方程，选拟合度最优者。')
bd('（2）共同点：均可采用剩余平方和Q进行判定，Q越小拟合度越高。')
bd('（3）不同点：线性回归用决定系数r²判定；非线性回归用相关指数R²或矫正后的拟合优度指标判定。')

bd('9. 根据你所学，陈述组距式分组法的基本步骤和注意事项？')
bd('答：步骤（6步）：确定全距→确定组数→确定组距→确定组中值→确定各组上下限→按观测值大小归组并制作次数分布表。')
bd('注意事项：组距有小数时进位取整；第一组组中值最好接近最小值；第一组下限须包含最小值，最后一组上限须大于最大值；前一组上限等于下一组下限；为避免数据同时出现在两个组，上限为整数时减0.1、一位小数减0.01、两位小数减0.001。')

bd('10. 根据你所学，陈述显著性检验的基本解题思路和步骤？')
bd('答：假设检验根据总体的理论分布和小概率原理，对未知总体提出两种对立的假设，由样本实际结果经计算做出推断。')
bd('步骤：①提出无效假设H₀和备择假设Hₐ；②确定显著性水平α；③在H₀正确前提下计算检验统计量；④根据α找出临界值，确定否定域，对H₀进行判断。')

bd('11. 假设检验两类错误的实质分别是什么？如何减少两类错误的发生？')
bd('答：第一类错误（α错误）：以真为假，即拒绝正确的H₀；第二类错误（β错误）：以假为真，即接受错误的H₀。')
bd('减少方法：在α已确定（0.05或0.01）的前提下，增加样本容量n可减小标准误（σ/√n），从而减小β值。')

bd('12. 方差分析的基本假定是什么？常采用哪些方法进行数据变换？')
bd('答：基本假定：①分布正态性；②方差同质性；③因子效应的加和性。')
