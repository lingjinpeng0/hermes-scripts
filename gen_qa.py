add_ap(3, '降低被掠食者发现概率理论: 聚集群体通过"稀释效应"(dilution effect)和"混淆效应"(confusion effect)有效降低掠食者攻击成功率。大型集群使捕食者难以锁定单个目标。')
add_parse('三大理论分别从行为学、流体力学和生态学角度解释了集群现象, 三者互补不矛盾。')

