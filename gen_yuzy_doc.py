    h2(doc, '3.5 思考题')

    q(doc, 1, '补充群体在生物学与渔业上的含义有何不同？')
    ans(doc, '答案:')
    ap(doc, 1, '生物学含义：指达到性成熟、首次参与繁殖的个体。从生物学角度完成了性腺发育并具备繁殖能力，是种群延续的关键组分。')
    ap(doc, 2, '渔业含义：指达到可捕规格（超过最小可捕体长或年龄）并进入渔场的个体。在渔业上具有捕捞价值，进入捕捞群体的范围。')
    ap(doc, 3, '差异与应用：生物学补充与渔业补充在时间上通常不一致——鱼类可能在达到性成熟后还需一段时间生长才能达到可捕规格。合理的管理策略应确保个体至少有一次繁殖机会后再被捕捞，即最小可捕体长应设定在性成熟体长以上，这有助于维持种群的补充能力。')
    exp(doc, '正确理解和区分补充群体的两种含义，是确定最小网目尺寸和开捕规格的理论基础，对渔业可持续发展至关重要。')

    q(doc, 2, 'Russell模型的基本形式及其各参数的含义是什么？如何利用该模型判断资源量的变化趋势？')
    ans(doc, '答案:')
    ap(doc, 1, '公式：B\\u2082 = B\\u2081 + R + G - M - Y')
    ap(doc, 2, '参数：B\\u2082期未资源量；B\\u2081期初资源量；R补充量（新加入个体的数量）；G生长增量（个体生物量的增加总和）；M自然死亡量（疾病、被捕食、衰老等非捕捞因素导致的死亡）；Y渔获量（捕捞活动导致的死亡量）。')
    ap(doc, 3, '判断方法：当R + G - M > Y时，B\\u2082 > B\\u2081，资源量增长；当R + G - M < Y时，B\\u2082 < B\\u2081，资源量衰退，即出现捕捞过度。')
    exp(doc, 'Russell模型是所有渔业资源变动模型的基础框架，后来的剩余产量模型和动态综合模型都是在其基础上发展深化而来。')

    q(doc, 3, '影响渔业资源数量变动的主要因素有哪些？分析时应如何处理？')
