# ── Three-Line Table ──────────────────────────────────────────────
def tbl(doc, headers, rows, caption=None):
    if caption:
        p = doc.add_paragraph()
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        r = p.add_run(caption)
        sf(r, size=Pt(10.5))
        p.paragraph_format.space_after = Pt(4)
        p.paragraph_format.first_line_indent = None
    t = doc.add_table(rows=1+len(rows), cols=len(headers))
    t.alignment = WD_ALIGN_PARAGRAPH.CENTER
    tbl_el = t._tbl
    tblPr = tbl_el.tblPr
    if tblPr is None:
        tblPr = OxmlElement('w:tblPr')
        tbl_el.insert(0, tblPr)
    tw = OxmlElement('w:tblW')
    tw.set(qn('w:w'), '5000')
    tw.set(qn('w:type'), 'pct')
    tblPr.append(tw)
    tb = OxmlElement('w:tblBorders')
    for bn in ['top','left','bottom','right','insideH','insideV']:
        b = OxmlElement(f'w:{bn}')
        b.set(qn('w:val'), 'nil')
        tb.append(b)
    tblPr.append(tb)
    # Header row borders
    for ci, hd in enumerate(headers):
        c = t.rows[0].cells[ci]
        c.text = ''
        p = c.paragraphs[0]
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        p.paragraph_format.first_line_indent = Pt(0)
        r = p.add_run(hd)
        sf(r, size=Pt(10))
        _border(c, top_sz='12', bot_sz='6')
    # Data rows
    for ri, row in enumerate(rows):
        for ci, val in enumerate(row):
            c = t.rows[ri+1].cells[ci]
            c.text = ''
            p = c.paragraphs[0]
            p.alignment = WD_ALIGN_PARAGRAPH.CENTER if len(str(val)) < 30 else WD_ALIGN_PARAGRAPH.LEFT
            p.paragraph_format.first_line_indent = Pt(0)
            r = p.add_run(str(val))
            sf(r, size=Pt(9))
    # Last row bottom thick
    for ci in range(len(headers)):
        c = t.rows[-1].cells[ci]
        tcPr = c._tc.get_or_add_tcPr()
        tcB = OxmlElement('w:tcBorders')
        bot = OxmlElement('w:bottom')
        bot.set(qn('w:val'), 'single')
        bot.set(qn('w:sz'), '12')
        bot.set(qn('w:space'), '0')
        bot.set(qn('w:color'), '000000')
        tcB.append(bot)
        tcPr.append(tcB)
    # cantSplit on every row
    for row in tbl_el.findall(qn('w:tr')):
        trPr = row.find(qn('w:trPr'))
        if trPr is None:
            trPr = OxmlElement('w:trPr')
            row.insert(0, trPr)
        old = trPr.find(qn('w:cantSplit'))
        if old is not None:
            trPr.remove(old)
        cs = OxmlElement('w:cantSplit')
        cs.set(qn('w:val'), '1')
        trPr.append(cs)

def _border(cell, top_sz='12', bot_sz='6'):
    tcPr = cell._tc.get_or_add_tcPr()
    tcB = OxmlElement('w:tcBorders')
    top = OxmlElement('w:top')
    top.set(qn('w:val'), 'single'); top.set(qn('w:sz'), top_sz)
    top.set(qn('w:space'), '0'); top.set(qn('w:color'), '000000')
    tcB.append(top)
    bot = OxmlElement('w:bottom')
    bot.set(qn('w:val'), 'single'); bot.set(qn('w:sz'), bot_sz)
    bot.set(qn('w:space'), '0'); bot.set(qn('w:color'), '000000')
    tcB.append(bot)
    tcPr.append(tcB)

# ── QA Helpers ────────────────────────────────────────────────────
def q(doc, num, text):
    p = doc.add_paragraph()
    r = p.add_run(f'{num}. {text}')
    sf(r, size=Pt(11))
    p.paragraph_format.space_before = Pt(8)
    p.paragraph_format.space_after = Pt(2)
    p.paragraph_format.first_line_indent = None

def ans(doc, text):
    p = doc.add_paragraph()
    p.paragraph_format.first_line_indent = Pt(21)
    r = p.add_run(text)
    sf(r, size=Pt(10.5))

def ap(doc, num, text):
    p = doc.add_paragraph()
    r = p.add_run(f'({num}) {text}')
    sf(r, size=Pt(10))
    p.paragraph_format.line_spacing = 1.2
    p.paragraph_format.space_after = Pt(2)
    pPr = p._p.get_or_add_pPr()
    ind = OxmlElement('w:ind')
    ind.set(qn('w:left'), '840')
    ind.set(qn('w:hanging'), '210')
    pPr.append(ind)

def exp(doc, text):
    p = doc.add_paragraph()
    p.paragraph_format.first_line_indent = Pt(21)
    r = p.add_run(f'解析: {text}')
    sf(r, size=Pt(10))

