# ── Font / Style Helpers ──────────────────────────────────────────
def sf(run, cn='宋体', en='Times New Roman', size=Pt(10.5), bold=False, italic=False):
    run.font.size = size
    run.font.name = en
    run.element.rPr.rFonts.set(qn('w:eastAsia'), cn)
    run.font.bold = bold
    run.font.italic = italic

def body(doc, text, indent=True, size=Pt(10.5)):
    p = doc.add_paragraph()
    r = p.add_run(text)
    sf(r, size=size)
    p.paragraph_format.line_spacing = 1.2
    p.paragraph_format.space_after = Pt(3)
    p.paragraph_format.first_line_indent = Pt(21) if indent else None
    return p

def h1(doc, text):
    p = doc.add_paragraph()
