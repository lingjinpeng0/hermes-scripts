    body(doc, '(1)地理隔离：海洋、山脉、河流等地理障碍导致种群间无法自由交配，长期的地理隔离促进种群间的遗传分化。(2)生殖隔离：由于生殖期不同（如产卵季节差异）、生殖行为差异或生殖器官结构差异等形成的隔离，即使在同一地理区域也能保持种群间的独立性。')
