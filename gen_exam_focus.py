add_bullet(2, '个体容量(Pitcher)——群体中一条鱼所占容量。绿线鳕最密集处1.4 BL³。')
add_bullet(3, '最小接近距离——沙丁鱼平均0.6 BL, 鲹鱼平均1.20 BL。')
add_bullet(4, '个体相对距离——各尾鱼与次最邻近鱼的距离和与最邻近鱼的距离的平均比值。接近1为均匀排列, 1.5为无规则排列。')
add_bullet(5, '偏好距离与偏好角度——个体间通常保持略大于最小接近距离, 约一倍鱼体长。')

