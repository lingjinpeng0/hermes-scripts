add_bullet(1, 'Schooling——带有方向性的集群, 基本同步、同方向游动。')
add_bullet(2, 'Shoaling——个体的聚集, 如摄食、索饵、产卵时形成的群体。')
add_bullet(3, 'Aggregation——偶然性、被动聚集的集合体。')

T('4.3 集群的机理（三大理论）', 2)
add_bullet(1, 'Parr理论(1927)——集群行为是个体间相互刺激产生的自动反应结果。视觉是保持个体间联系的主要手段。个体行为受"诱引力"和"排斥力"影响。群体分为偶然性群体和定常性群体。')
add_bullet(2, 'Weihs理论(1973, 1975)——应用流体力学和节能原理, 提出尾流理论。鱼以菱形分布游泳可节能65%, 外侧临近鱼距离为0.4倍鱼体长度, 鱼尾拍水相位应处于反相。')
add_bullet(3, '降低被掠食者发现概率理论——聚集群体能有效降低掠食者攻击成功率。')

T('4.4 感觉器官在集群中的作用（考点重点）', 2)
B('以下感觉器官与鱼类集群密切相关:')
add_bullet(1, '视觉——通过视觉运动反应保持平行状态和相互诱引。"保标性"使群体保持平行状态。')
add_bullet(2, '侧线——在弱光环境中对集群起决定性作用。失明鱼类仍能大致维持位置, 说明侧线是关键。')
add_bullet(3, '嗅觉——远距离化学感受器, 对摄食、集群、求偶、洄游有重要意义。夜间鱼群可能利用嗅觉保持集群。')
add_bullet(4, '电感受器——如电鳗等具有发电器官, 能瞬时释放大量电流。')

T('4.5 鱼群空间分布特征参数', 2)
