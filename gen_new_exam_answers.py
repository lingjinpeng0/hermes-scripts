sub_h('四、分析并改错（共12分）')

body('题：以下是某同学在统计分析中的一些说法，请判断正误，若错误请分析原因并改正。')

body('（1）"在网箱养鱼试验中，每个网箱中的每一条鱼均为一个试验单元。"')
body('答：错误。试验单元是试验人员施加处理的对象单位，在网箱养鱼试验中，一个网箱内全部鱼类个体组成一个试验单元，每一条鱼是抽样单元而非试验单元。改正：一个网箱内全部鱼为一个试验单元。')

body('（2）"零假设H₀被拒绝，这意味着H₀绝对错误。"')
body('答：错误。拒绝H₀是基于小概率原理（P<α），即在H₀正确的前提下，出现样本结果的可能性很小，因此拒绝H₀。但仍有可能犯第一类错误（以真为假），并非绝对。改正：H₀被拒绝意味着在α水平上差异显著，但不等于H₀绝对错误。')

body('（3）"概率是频率的极限。"')
body('答：错误。概率是频率的稳定值，但不是数学意义上的极限。随着试验次数n增大，频率在概率附近波动并趋于稳定，而非趋近于某一定值。改正：概率是频率的稳定值。')

body('（4）"在单因子方差分析中可能有固定效应模型、随机效应模型和混合模型。"')
body('答：错误。混合模型出现在多因子方差分析中（一个因子固定、一个因子随机）。单因子方差分析只有固定效应模型和随机效应模型两种。改正：单因子方差分析有固定效应模型和随机效应模型，混合模型仅存在于多因子方差分析中。')

body('（5）"回归方程显著，其实质是指回归系数b和回归系数a均显著。"')
body('答：错误。回归方程显著性检验（F检验）主要检验回归系数b（斜率）是否显著不为零，即自变量x对因变量y是否有显著影响。截距a一般不作为显著性检验的对象。改正：回归方程显著指回归系数b（斜率）显著不为零。')

